function findEmojiResponse(message) {
  if (!message) return null;
  
  for (const [emoji, responses] of Object.entries(emojiDatabase)) {
    if (message.includes(emoji)) {
      return responses[Math.floor(Math.random() * responses.length)];
    }
  }
  
  return null;
}

